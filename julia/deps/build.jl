# build.jl — produces deps.jl with the absolute path to libsu2.so
#
# Strategy: shell out to `make -C <project_root> build/libsu2.so`. The project
# root is two levels above this file. Write the absolute resolved path to
# deps.jl so SU2FFT.jl can include it. Bead: su2fft-t6z.

const PROJECT_ROOT = abspath(joinpath(@__DIR__, "..", ".."))
const SO_PATH      = joinpath(PROJECT_ROOT, "build", "libsu2.so")
const MAKE_TARGET  = joinpath("build", "libsu2.so")
const DEPS_FILE    = joinpath(@__DIR__, "deps.jl")

println("[SU2FFT build.jl] Building libsu2.so via make in $PROJECT_ROOT")

cmd = `make -C $PROJECT_ROOT $MAKE_TARGET`
run(cmd)

isfile(SO_PATH) || error("Build failed: $SO_PATH not found after make")

open(DEPS_FILE, "w") do io
    println(io, "# Generated by build.jl — do not edit by hand.")
    println(io, "const LIBSU2 = ", repr(SO_PATH))
end

println("[SU2FFT build.jl] Wrote $DEPS_FILE pointing at $SO_PATH")
